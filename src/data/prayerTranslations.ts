// Helper function to get translated prayer content
export function getTranslatedPrayer(prayerId: string, lang: string, originalPrayer: { title: string; meaning: string }) {
    // If Turkish, return original
    if (lang === 'tr') {
        return originalPrayer;
    }

    // Check if translation exists for current language
    if (prayerTranslations[lang] && prayerTranslations[lang][prayerId]) {
        return prayerTranslations[lang][prayerId];
    }

    // Fallback to English if available
    if (prayerTranslations['en'] && prayerTranslations['en'][prayerId]) {
        return prayerTranslations['en'][prayerId];
    }

    // Last resort: return original
    return originalPrayer;
}
